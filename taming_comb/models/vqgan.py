def get_obj_from_str(string, reload=False):
    module, cls = string.rsplit(".", 1)
    if reload:
        module_imp = importlib.import_module(module)
        importlib.reload(module_imp)
    return getattr(importlib.import_module(module, package=None), cls)



def instantiate_from_config(config):
    if not "target" in config:
        raise KeyError("Expected key `target` to instantiate.")
    return get_obj_from_str(config["target"])(**config.get("params", dict()))


class VQModel(nn.Module):
    def __init__(self,
                 ddconfig,
                 lossconfig,
                 n_embed,
                 embed_dim,
                 ckpt_path=None,
                 ignore_keys=[],
                 image_key="image",
                 colorize_nlabels=None,
                 monitor=None
                 ):
        super(VQModel, self).__init__()
        self.image_key = image_key
        self.encoder = Encoder(**ddconfig)
        self.decoder_a = Decoder(**ddconfig)
        self.decoder_b = Decoder(**ddconfig)
        self.loss_a = instantiate_from_config(lossconfig)
        self.loss_b = instantiate_from_config(lossconfig)
        self.quantize = VectorQuantizer(n_embed, embed_dim, beta=0.25)
        self.quant_conv = torch.nn.Conv2d(ddconfig["z_channels"], embed_dim, 1)
        self.post_quant_conv = torch.nn.Conv2d(embed_dim, ddconfig["z_channels"], 1)

    def encode(self, x):
        h = self.encoder(x)
        h = self.quant_conv(h)
        quant, emb_loss, info = self.quantize(h)
        return quant, emb_loss, info

    def decode_a(self, quant):
        quant = self.post_quant_conv(quant)
        dec = self.decoder_a(quant)
        return dec

    def decode_b(self, quant):
        quant = self.post_quant_conv(quant)
        dec = self.decoder_b(quant)
        return dec

    def decode_code(self, code_b):
        quant_b = self.quantize.embed_code(code_b)
        dec = self.decode(quant_b)
        return dec

    def forward(self, input, label):
        if(label == 1):
            quant, diff, _ = self.encode(input)
            dec = self.decode_a(quant)
        else:
            quant, diff, _ = self.encode(input)
            dec = self.decode_b(quant)

        return dec, diff

    def get_input(self, batch, k=None):
        x = batch[k]
        if len(x.shape) == 3:
            x = x[..., None]
        x = x.permute(0, 3, 1, 2).to(memory_format=torch.contiguous_format)
        return x.float()

    def get_last_layer(self, label):
        if(label ==  1):
            return self.decoder_a.conv_out.weight
        else:
            return self.decoder_b.conv_out.weight


